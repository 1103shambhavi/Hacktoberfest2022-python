#sort in ascending

#Sort number in ascending order
number = [1,2,4,5,3,7,6]

number.sort()

print("The numbers are sorted in ascending number ",number)

#Sort decimal number in ascending order
decimal = [1.1,2.1,4.3,9.4,6.3]

decimal.sort()

print("The numbers are sorted in ascending number ",decimal)

#Sort String in ascending order
sentence = ["word","sentence","test"]

sentence.sort()

print("The string is sorted in ascending number ",sentence)


#sort in descending

#Sort number in descending order
number = [1,2,4,5,3,7,6]

number.sort(reverse=True)

print("The numbers are sorted in ascending number ",number)

#Sort decimal number in descending order
decimal = [1.1,2.1,4.3,9.4,6.3]

decimal.sort(reverse=True)

print("The numbers are sorted in ascending number ",decimal)

#Sort String in descending order
sentence = ["word","sentence","test"]

sentence.sort(reverse=True)

print("The string is sorted in ascending number ",sentence)